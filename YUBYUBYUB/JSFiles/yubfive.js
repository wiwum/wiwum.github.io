var yubnum = Math.random();

function howmanyyub() {
   while (yubnum < .3333333333) {
      alert("YUB");
      yubnum = .3333333333;
   }
   while (yubnum > .3333333333 & yubnum < .6666666666) {
      alert("YUBYUB");
      yubnum = .3333333333;
   }
   while (yubnum > .6666666666) {
      alert("YUBYUBYUB");
      yubnum = .3333333333;
   }
}
